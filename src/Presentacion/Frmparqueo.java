    private void btnRegistrarIngresoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarIngresoActionPerformed
        try {
            service.registrarIngreso(
                    txtPlaca.getText(),
                    cmbTipo.getSelectedItem().toString()
            );

            lblMensaje.setText("Ingreso registrado correctamente");
            txtPlaca.setText("");
            cargarVehiculosActivos();

        } catch (Exception e) {
            lblMensaje.setText(e.getMessage());
        }
    }//GEN-LAST:event_btnRegistrarIngresoActionPerformed

    private void btnRegistrarSalidaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarSalidaActionPerformed

        int fila = tblActivos.getSelectedRow();

        if (fila == -1) {
            lblMensaje.setText("Seleccione un vehículo");
            return;
        }

        String placa = tblActivos
                .getValueAt(fila, 0)
                .toString();

        try {
            double monto = service.registrarSalida(placa);
            lblMensaje.setText("Monto a pagar: ₡" + monto);
            cargarVehiculosActivos();

        } catch (Exception e) {
            lblMensaje.setText(e.getMessage());
        }
    }//GEN-LAST:event_btnRegistrarSalidaActionPerformed

