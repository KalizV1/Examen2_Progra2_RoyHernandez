    private void cargarVehiculosActivos() {
        try {
            modeloTabla.setRowCount(0);

            java.util.List<entidades.RegistroParqueo> lista
                    = service.obtenerActivos();

            for (entidades.RegistroParqueo r : lista) {
                modeloTabla.addRow(new Object[]{
                    r.getVehiculo().getPlaca(),
                    r.getVehiculo().getTipo()
                });
            }

        } catch (Exception e) {
            lblMensaje.setText(e.getMessage());
        }
    }

